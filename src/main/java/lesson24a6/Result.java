package lesson24a6;

public class Result {
    boolean found;
    int count;
    int index;

    public void setFound(boolean found) {
        this.found = found;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public boolean isFound() {
        return found;
    }

    public int getCount() {
        return count;
    }

    public int getIndex() {
        return index;
    }
}
